import { get } from 'actions/common/async-action';


export const NON_FEATURED_STORIES_REQUEST_START = 'NON_FEATURED_STORIES_REQUEST_START';
export const NON_FEATURED_STORIES_REQUEST_SUCCESS = 'NON_FEATURED_STORIES_REQUEST_SUCCESS';
export const NON_FEATURED_STORIES_REQUEST_FAILURE = 'NON_FEATURED_STORIES_REQUEST_FAILURE';

export const STORIES_API_URL = 'stories/';

export const requestStories = get(
  STORIES_API_URL,
  [NON_FEATURED_STORIES_REQUEST_START, NON_FEATURED_STORIES_REQUEST_SUCCESS, NON_FEATURED_STORIES_REQUEST_FAILURE]
);

export const LOAD_MORE_STORIES_REQUEST_START = 'LOAD_MORE_STORIES_REQUEST_START';
export const LOAD_MORE_STORIES_REQUEST_SUCCESS = 'LOAD_MORE_STORIES_REQUEST_SUCCESS';
export const LOAD_MORE_STORIES_REQUEST_FAILURE = 'LOAD_MORE_STORIES_REQUEST_FAILURE';

export const loadMoreStories = (nextUrl) => {
  return get(
    nextUrl,
    [LOAD_MORE_STORIES_REQUEST_START, LOAD_MORE_STORIES_REQUEST_SUCCESS, LOAD_MORE_STORIES_REQUEST_FAILURE]
  )();
};
